package LP1Classes;

public class Calculadora {
    public Calculadora() {

    }
    public void somar(int number1, int number2){
        System.out.println("Valor da soma"+ (number1 + number2));
    }
    public void subtrair(int number1, int number2){
        System.out.println("Valor da subtração"+ (number1 - number2));
    }

    public void multiplicar(int number1, int number2){
        System.out.println("Valor da multiplicar"+ (number1 * number2));
    }

    public void divisao(int number1, int number2){
        System.out.println("Valor de divisão"+ (number1 / number2));
    }

}
